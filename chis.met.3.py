# Задание 2: Решение системы методом Зейделя с точностью epsilon=0.001

# Начальные приближения
x = [0.0, 0.0, 0.0]

# Точность
epsilon = 0.001

# Максимальное число итераций
max_iterations = 1000

for iteration in range(max_iterations):
    x_prev = x.copy()

    # Обновление по формулам (метод Зейделя), использую уже обновленные значения
    # Вычисляем x_1
    x[0] = (2.4 + 2.3 * x[1] - 4.5 * x[2]) / 3.7

    # Вычисляем x_2
    x[1] = (3.5 - 2.5 * x[0] + 7.8 * x[2]) / 4.7

    # Вычисляем x_3
    x[2] = (-2.4 - 1.6 * x[0] - 5.3 * x[1]) / 1.3

    # Проверка сходимости
    if max(abs(x[i] - x_prev[i]) for i in range(3)) < epsilon:
        break

# Вывод результата
print(f"Метод Зейделя победил за {iteration+1} итераций.")
print(f"x1 = {x[0]:.4f}")
print(f"x2 = {x[1]:.4f}")
print(f"x3 = {x[2]:.4f}")
